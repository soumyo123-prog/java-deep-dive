package day2;

class Person {
	// Declaring all the variables as private, and accessing them through
	// getters and setters. Very straight forward.

	private String name;
	private int age;
	private int jobId;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getJobId() {
		return jobId;
	}

	public void setJobId(int jobId) {
		this.jobId = jobId;
	}

}

